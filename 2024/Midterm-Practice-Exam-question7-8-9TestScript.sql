    employee_id INT PRIMARY KEY,
    employee_name TEXT NOT NULL,
    department TEXT NOT NULL
);

CREATE TABLE Salary (
    salary_id INT PRIMARY KEY,
    employee_id INT,
    department TEXT NOT NULL,
    salary_amount DECIMAL(10, 2) NOT NULL,
    FOREIGN KEY (employee_id) REFERENCES Employee(employee_id)
);

